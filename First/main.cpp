void DrawMaze()
{
	int i, j;

	for(i = 0;i<MSIZE;i++)
		for (j = 0; j < MSIZE; j++)
		{
			switch (maze[i][j])
			{
			case WALL:
				glColor3d(0.4, 0, 0); // dark red;
				break;
			case SPACE:
				glColor3d(1, 1, 1); // white;
				break;
			case VISITED:
				glColor3d(0, 0.9, 0); // green;
				break;
			case START:
				glColor3d(0, 0, 1); // blue;
				break;
			case TARGET:
				glColor3d(1,0,0 ); // RED;
				break;
			case GRAY:
				glColor3d(1, .8, 0); // ORANGE;
				break;
			case PATH:
				glColor3d(0.8, .5, 1); // ORANGE;
				break;
			case FIGURE1:
				glColor3d(0, 0, 0);
				break;
			case FIGURE2:
				glColor3d(0, 0, 0);
				break;
			case AMMO:
				glColor3d(0, 0.9, 0);
				break;
			case HEALTH:
				glColor3d(0, 0, 1);
				break;
			}

			// draw square
			glBegin(GL_POLYGON);
				glVertex2d(j*SQSIZE - 1- SQSIZE/2, i*SQSIZE - 1+SQSIZE/2);
				glVertex2d(j*SQSIZE - 1 + SQSIZE / 2, i*SQSIZE - 1 + SQSIZE / 2);
				glVertex2d(j*SQSIZE - 1 + SQSIZE / 2, i*SQSIZE - 1 - SQSIZE / 2);
				glVertex2d(j*SQSIZE - 1 - SQSIZE / 2, i*SQSIZE - 1 - SQSIZE / 2);
			glEnd();
		}

}

