void AStarIteration(Point2D* parentAStar[MSIZE][MSIZE], int(*mazeFigure)[MSIZE][MSIZE], int figureTarget, int figureStart, Point2D* target, bool* astar_started)
{
	Point2D* pt;
	Point2D* pt1;
	if (pq.empty())
	{
		(*astar_started) = false;// there is no path to the target
	}
	else // gray is not empty
	{
		pt = pq.top(); // this will be the parent
		pq.pop(); // dequeue
				  // paint pt VISITED
		if ((*mazeFigure)[pt->GetY()][pt->GetX()] == figureTarget) // we have found the target
		{
			(*astar_started) = false;
		}
		else
		{
			if ((*mazeFigure)[pt->GetY()][pt->GetX()] != figureStart)
				(*mazeFigure)[pt->GetY()][pt->GetX()] = VISITED; // y is i, x is j!!! 
														// check non-visited neighbors
														// go up
			if ((*mazeFigure)[pt->GetY() + 1][pt->GetX()] == figureTarget)
			{
				(*astar_started) = false;

			}
			if ((*astar_started) && (*mazeFigure)[pt->GetY() + 1][pt->GetX()] == SPACE)
			{ // add it to gray
				(*mazeFigure)[pt->GetY() + 1][pt->GetX()] = GRAY;
				parentAStar[pt->GetY() + 1][pt->GetX()] = pt;
				pt1 = new Point2D(pt->GetX(), pt->GetY() + 1);// y is i, x is j!!! 
				pt1->setH(*target);
				pq.push(pt1);
			}
			// go down
			if ((*mazeFigure)[pt->GetY() - 1][pt->GetX()] == figureTarget)
			{
				(*astar_started) = false;

			}
			if ((*astar_started) && (*mazeFigure)[pt->GetY() - 1][pt->GetX()] == SPACE)
			{ // add it to gray
				(*mazeFigure)[pt->GetY() - 1][pt->GetX()] = GRAY;
				parentAStar[pt->GetY() - 1][pt->GetX()] = pt;
				pt1 = new Point2D(pt->GetX(), pt->GetY() - 1);// y is i, x is j!!! 
				pt1->setH(*target);
				pq.push(pt1);
			}
			// go right
			if ((*mazeFigure)[pt->GetY()][pt->GetX() + 1] == figureTarget)
			{
				(*astar_started) = false;

			}
			if ((*astar_started) && (*mazeFigure)[pt->GetY()][pt->GetX() + 1] == SPACE)
			{ // add it to gray
				parentAStar[pt->GetY()][pt->GetX() + 1] = pt;
				(*mazeFigure)[pt->GetY()][pt->GetX() + 1] = GRAY;
				pt1 = new Point2D(pt->GetX() + 1, pt->GetY());// y is i, x is j!!! 
				pt1->setH(*target);
				pq.push(pt1);
			}
			// go left
			if ((*astar_started) && (*mazeFigure)[pt->GetY()][pt->GetX() - 1] == figureTarget)
			{
				(*astar_started) = false;

			}
			if ((*astar_started) && (*mazeFigure)[pt->GetY()][pt->GetX() - 1] == SPACE)
			{ // add it to gray
				(*mazeFigure)[pt->GetY()][pt->GetX() - 1] = GRAY;
				parentAStar[pt->GetY()][pt->GetX() - 1] = pt;
				pt1 = new Point2D(pt->GetX() - 1, pt->GetY());// y is i, x is j!!! 
				pt1->setH(*target);
				pq.push(pt1);
			}
			if (!(*astar_started)) // target was found
				ShowPathAStar(parentAStar, mazeFigure, pt, figureStart);
		}
	}
}

Point2D* getObject(priority_queue <Point2D, vector <Point2D*>, CompareNodes>* pq, Figure* figure)
{
	Point2D* p;
	priority_queue <Point2D, vector <Point2D*>, CompareNodes> tempPq;
	while (!pq->empty())
	{
		p = pq->top();
		pq->pop();
		p->setH(*(figure->getPoint()));
		tempPq.push(p);
	}
	while (!tempPq.empty())
	{
		p = tempPq.top();
		tempPq.pop();
		pq->push(p);
	}
	p = pq->top();
	pq->pop();
	return p;
}

void fight(Figure* figure, Figure* rival)
{
	double damage = 0.0;
	int startX, endX, startY, endY, numOfSpace = 0, rivalX, rivalY, figureX, figureY, empty = 40;
	rivalX = rival->getPoint()->GetX();
	rivalY = rival->getPoint()->GetY();
	figureX = figure->getPoint()->GetX();
	figureY = figure->getPoint()->GetY();
	if (figureX > rivalX)
	{
		endX = figureX;
		startX = rivalX;
	}
	else
	{
		startX = figureX;
		endX = rivalX;
	}
	if (figureY > rivalY)
	{
		endY = figureY;
		startY = rivalY;
	}
	else
	{
		startY = figureY;
		endY = rivalY;
	}
	for (int i = startX; i < endX; i++)
	{
		for (int j = startY; j < endY; j++)
		{
			if (maze[i][j] == SPACE)
			{
				empty--;
			}
		}
	}
	damage = sqrt(pow(figureX - rivalX, 2) + pow(figureY - rivalY, 2)) * 0.1 * empty;
	int d = (int)damage;
	if (damage > 20)
		d /= 20;
	if (figure->getAmmo() > 0)
	{
		cout << "Figure" << figure->id << " Shot on rival and make damage : " << d << endl;
		rival->removeHealth(d);
		figure->removeAmmo(1);
	}
}

