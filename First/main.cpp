void moveFigure(int (*mazeFigure)[MSIZE][MSIZE],Figure* figure,int start,int target)
{
	Point2D* pt;
	int x, y;
	x = figure->getPoint()->GetX();
	y = figure->getPoint()->GetY();
	if ((*mazeFigure)[y][x] == target) // Figure got to target
	{
		figureGotToTarget = true;
		// Grab the target (health,ammo)
	}
	else // moving on the gray path
	{
		if ((*mazeFigure)[y + 1][x] == PATH || (*mazeFigure)[y + 1][x] == target)
		{
			maze[y][x] = SPACE;
			(*mazeFigure)[y][x] = SPACE;
			y++;
		}
		if ((*mazeFigure)[y - 1][x] == PATH || (*mazeFigure)[y - 1][x] == target)
		{
			maze[y][x] = SPACE;
			(*mazeFigure)[y][x] = SPACE;
			y--;
		}
		if ((*mazeFigure)[y][x + 1] == PATH || (*mazeFigure)[y][x + 1] == target)
		{
			maze[y][x] = SPACE;
			(*mazeFigure)[y][x] = SPACE;
			x++;
		}
		if ((*mazeFigure)[y][x - 1] == PATH || (*mazeFigure)[y][x - 1] == target)
		{
			maze[y][x] = SPACE;
			(*mazeFigure)[y][x] = SPACE;
			x--;
		}
		pt = new Point2D(x, y);
		figure->setPoint(pt);
		maze[y][x] = start;
		(*mazeFigure)[y][x] = start;
	}
}

